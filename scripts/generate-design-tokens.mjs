#!/usr/bin/env node
/**
 * Generate platform design-token outputs from design/tokens.json.
 * Run: npm run tokens:generate (from web/) or node scripts/generate-design-tokens.mjs
 */
import { mkdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const TOKENS_PATH = path.join(ROOT, "design", "tokens.json");
const WEB_CSS_PATH = path.join(ROOT, "web", "src", "styles", "tokens.generated.css");
const IOS_COLORS_DIR = path.join(ROOT, "ios", "TTF", "TTF", "Resources", "Colors.xcassets");
const IOS_THEME_PATH = path.join(ROOT, "ios", "TTF", "TTF", "Utilities", "Theme.swift");
const IOS_TTF_TIER_PATH = path.join(ROOT, "ios", "TTF", "TTF", "Utilities", "TtfTier.swift");
const WEB_TTF_TIER_PATH = path.join(ROOT, "web", "src", "lib", "ttfTier.ts");
const WEB_MAP_PIN_PATH = path.join(ROOT, "web", "src", "lib", "mapPin.ts");
const WEB_OVERLAY_STACK_PATH = path.join(ROOT, "web", "src", "lib", "overlayStack.ts");

function camelToKebab(key) {
  return key.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase();
}

function camelToPascal(key) {
  return key.charAt(0).toUpperCase() + key.slice(1);
}

function hexToRgb(hex) {
  const h = hex.replace("#", "");
  const r = parseInt(h.slice(0, 2), 16) / 255;
  const g = parseInt(h.slice(2, 4), 16) / 255;
  const b = parseInt(h.slice(4, 6), 16) / 255;
  return { r, g, b };
}

function rgbComponents(hex) {
  const { r, g, b } = hexToRgb(hex);
  return {
    alpha: "1.000",
    red: r.toFixed(3),
    green: g.toFixed(3),
    blue: b.toFixed(3),
  };
}

function colorsetContents(lightHex, darkHex) {
  return {
    colors: [
      {
        color: { "color-space": "srgb", components: rgbComponents(lightHex) },
        idiom: "universal",
      },
      {
        color: { "color-space": "srgb", components: rgbComponents(darkHex) },
        idiom: "universal",
        appearances: [{ appearance: "luminosity", value: "dark" }],
      },
    ],
    info: { author: "generate-design-tokens", version: 1 },
  };
}

function generateWebCss(tokens) {
  const lines = [
    "/* Generated by scripts/generate-design-tokens.mjs — do not edit. */",
    "",
    "@theme {",
  ];

  for (const [key, value] of Object.entries(tokens.color)) {
    const cssKey = camelToKebab(key);
    lines.push(`  --color-${cssKey}: ${value.light};`);
  }

  lines.push(`  --font-sans: ${tokens.font.webSans};`);
  lines.push(`  --font-display: ${tokens.font.webDisplay};`);

  if (tokens.typography) {
    for (const [key, value] of Object.entries(tokens.typography)) {
      lines.push(`  --text-${camelToKebab(key)}: ${value};`);
    }
  }

  if (tokens.paper) {
    for (const [key, value] of Object.entries(tokens.paper)) {
      lines.push(`  --paper-${camelToKebab(key)}: ${value};`);
    }
  }

  for (const [key, value] of Object.entries(tokens.radius)) {
    lines.push(`  --radius-${key}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.spacing)) {
    lines.push(`  --spacing-${key}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.shadow)) {
    lines.push(`  --shadow-${key}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.layout)) {
    lines.push(`  --${camelToKebab(key)}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.motion)) {
    lines.push(`  --${camelToKebab(key)}: ${value};`);
  }

  if (tokens.zIndex) {
    for (const [key, value] of Object.entries(tokens.zIndex)) {
      lines.push(`  --z-${camelToKebab(key)}: ${value};`);
    }
  }

  lines.push("}", "", ".dark {");

  for (const [key, value] of Object.entries(tokens.color)) {
    const cssKey = camelToKebab(key);
    lines.push(`  --color-${cssKey}: ${value.dark};`);
  }

  lines.push("}", "");
  return lines.join("\n");
}

function generateThemeSwift(colorKeys, font) {
  const props = colorKeys
    .map((key) => `    static let ${key} = Color("${camelToPascal(key)}")`)
    .join("\n");

  const displayFamily = font?.iosDisplay ?? "Quicksand";
  const bodyFamily = font?.iosSans ?? "Nunito";

  return `// Generated by scripts/generate-design-tokens.mjs — do not edit.

import SwiftUI
import UIKit

extension Color {
${props}
}

// MARK: - Brand type (Bluebird: ${displayFamily} display / ${bodyFamily} body)
//
// Static-weight TTFs are bundled in TTF/Resources/Fonts and registered via
// UIAppFonts in Info.plist. Every helper falls back gracefully to the system
// font (rounded design for display) when a face is missing, so the app never
// crashes or renders blank text if the fonts fail to register.

extension Font {
    /// ${displayFamily} — display, headings, big TTF numbers.
    static func lsDisplay(_ size: CGFloat, weight: Font.Weight = .semibold) -> Font {
        LSBrandFont.font(family: .display, size: size, weight: weight)
    }

    /// ${bodyFamily} — UI and body copy.
    static func lsBody(_ size: CGFloat, weight: Font.Weight = .regular) -> Font {
        LSBrandFont.font(family: .body, size: size, weight: weight)
    }

    // Semantic shortcuts (Bluebird type scale, tokens/typography.css).
    /// Page titles — ${displayFamily} 34 bold.
    static let lsLargeTitle = lsDisplay(34, weight: .bold)
    /// Section titles (h1) — ${displayFamily} 26 bold.
    static let lsTitle = lsDisplay(26, weight: .bold)
    /// Sub-section titles (h2) — ${displayFamily} 21 bold.
    static let lsTitle2 = lsDisplay(21, weight: .bold)
    /// Card / row headlines (h3) — ${displayFamily} 18 semibold.
    static let lsHeadline = lsDisplay(18, weight: .semibold)
    /// Flagship TTF figure — ${displayFamily} 38 bold.
    static let lsNumber = lsDisplay(38, weight: .bold)
    /// Body copy — ${bodyFamily} 15 regular.
    static let lsBodyText = lsBody(15)
    /// Secondary copy / captions — ${bodyFamily} 13 semibold.
    static let lsCaption = lsBody(13, weight: .semibold)
    /// Eyebrow labels — ${bodyFamily} 11 extra-bold (track + uppercase at call site).
    static let lsLabel = lsBody(11, weight: .heavy)
}

/// Resolves bundled brand faces with graceful system fallback.
enum LSBrandFont {
    enum Family {
        case display
        case body
    }

    static let displayFamily = "${displayFamily}"
    static let bodyFamily = "${bodyFamily}"

    /// PostScript names of the faces actually registered at runtime.
    private static let availableFaces: Set<String> = {
        let candidates = [
            "${displayFamily}-Medium", "${displayFamily}-SemiBold", "${displayFamily}-Bold",
            "${bodyFamily}-Regular", "${bodyFamily}-Medium", "${bodyFamily}-SemiBold",
            "${bodyFamily}-Bold", "${bodyFamily}-ExtraBold",
        ]
        return Set(candidates.filter { UIFont(name: $0, size: 12) != nil })
    }()

    static func font(family: Family, size: CGFloat, weight: Font.Weight) -> Font {
        let name = faceName(family: family, weight: weight)
        guard availableFaces.contains(name) else {
            return .system(size: size, weight: weight, design: family == .display ? .rounded : .default)
        }
        return .custom(name, size: size)
    }

    private static func faceName(family: Family, weight: Font.Weight) -> String {
        switch family {
        case .display:
            // Bundled: Medium (500), SemiBold (600), Bold (700).
            switch weight {
            case .ultraLight, .thin, .light, .regular, .medium: return "\\(displayFamily)-Medium"
            case .semibold: return "\\(displayFamily)-SemiBold"
            default: return "\\(displayFamily)-Bold"
            }
        case .body:
            // Bundled: Regular (400), Medium (500), SemiBold (600), Bold (700), ExtraBold (800).
            switch weight {
            case .ultraLight, .thin, .light, .regular: return "\\(bodyFamily)-Regular"
            case .medium: return "\\(bodyFamily)-Medium"
            case .semibold: return "\\(bodyFamily)-SemiBold"
            case .bold: return "\\(bodyFamily)-Bold"
            default: return "\\(bodyFamily)-ExtraBold"
            }
        }
    }
}
`;
}

function generateTtfTierSwift() {
  return `// Generated by scripts/generate-design-tokens.mjs — do not edit.

import SwiftUI

enum TtfTier: String, CaseIterable {
    case fast
    case ok
    case slow
    case unknown

    var label: String {
        switch self {
        case .fast: "Fast (≤8 min)"
        case .ok: "OK (9–15 min)"
        case .slow: "Slow (>15 min)"
        case .unknown: "Not enough data"
        }
    }

    var color: Color {
        switch self {
        case .fast: .ttfFast
        case .ok: .ttfOk
        case .slow: .ttfSlow
        case .unknown: .ttfUnknown
        }
    }
}

enum TtfTierLogic {
    static func tier(for ttf: TtfAggregate) -> TtfTier {
        guard ttf.sampleSize >= 3, let median = ttf.medianMinutes else {
            return .unknown
        }
        if median <= 8 { return .fast }
        if median <= 15 { return .ok }
        return .slow
    }

    static func formattedMedian(_ ttf: TtfAggregate) -> String {
        guard ttf.sampleSize > 0, let median = ttf.medianMinutes else {
            return "—"
        }
        return "\\(Int(median.rounded())) min"
    }
}
`;
}

function generateWebTtfTier(tokens) {
  const c = tokens.color;
  return `// Generated by scripts/generate-design-tokens.mjs — do not edit.

import type { TtfAggregate } from "../types";

export type TtfTier = "fast" | "ok" | "slow" | "unknown";

export const TTF_TIER_COLORS: Record<TtfTier, string> = {
  fast: "${c.ttfFast.light}",
  ok: "${c.ttfOk.light}",
  slow: "${c.ttfSlow.light}",
  unknown: "${c.ttfUnknown.light}",
};

export const TTF_TIER_LABELS: Record<TtfTier, string> = {
  fast: "Fast (≤8 min)",
  ok: "OK (9–15 min)",
  slow: "Slow (>15 min)",
  unknown: "Not enough data",
};

export function ttfTier(ttf: TtfAggregate): TtfTier {
  if (ttf.sample_size < 3) return "unknown";
  const median = ttf.median_minutes;
  if (median === null) return "unknown";
  if (median <= 8) return "fast";
  if (median <= 15) return "ok";
  return "slow";
}

export function ttfTierColor(ttf: TtfAggregate): string {
  return TTF_TIER_COLORS[ttfTier(ttf)];
}

export function formatTtfMedian(ttf: TtfAggregate): string {
  if (ttf.sample_size === 0 || ttf.median_minutes === null) return "—";
  return \`\${Math.round(ttf.median_minutes)} min\`;
}
`;
}

async function writeIosColorsets(tokens) {
  await rm(IOS_COLORS_DIR, { recursive: true, force: true });
  await mkdir(IOS_COLORS_DIR, { recursive: true });
  await writeFile(
    path.join(IOS_COLORS_DIR, "Contents.json"),
    JSON.stringify(
      { info: { author: "generate-design-tokens", version: 1 } },
      null,
      2,
    ) + "\n",
  );

  for (const [key, value] of Object.entries(tokens.color)) {
    const dir = path.join(IOS_COLORS_DIR, `${camelToPascal(key)}.colorset`);
    await mkdir(dir, { recursive: true });
    await writeFile(
      path.join(dir, "Contents.json"),
      JSON.stringify(colorsetContents(value.light, value.dark), null, 2) + "\n",
    );
  }

  // AccentColor mirrors brand for global SwiftUI tint
  const accentDir = path.join(
    ROOT,
    "ios",
    "TTF",
    "TTF",
    "Resources",
    "Assets.xcassets",
    "AccentColor.colorset",
  );
  await writeFile(
    path.join(accentDir, "Contents.json"),
    JSON.stringify(colorsetContents(tokens.color.brand.light, tokens.color.brand.dark), null, 2) +
      "\n",
  );
}

async function main() {
  const raw = await readFile(TOKENS_PATH, "utf8");
  const tokens = JSON.parse(raw);
  const colorKeys = Object.keys(tokens.color);

  await writeFile(WEB_CSS_PATH, generateWebCss(tokens));
  await writeIosColorsets(tokens);
  await writeFile(IOS_THEME_PATH, generateThemeSwift(colorKeys, tokens.font));
  await writeFile(IOS_TTF_TIER_PATH, generateTtfTierSwift());
  await writeFile(WEB_TTF_TIER_PATH, generateWebTtfTier(tokens));

  const c = tokens.color;
  const mapPin = await readFile(WEB_MAP_PIN_PATH, "utf8");
  const pinColorsBlock = `/** Map pin colors — generated from design tokens (light values). */
export const SEARCH_FOCUS_PIN_COLOR = "${c.pinSearchFocus.light}";
`;
  if (!mapPin.includes("SEARCH_FOCUS_PIN_COLOR")) {
    console.warn("mapPin.ts: pin color exports not found — skip patch");
  } else {
    const updated = mapPin.replace(
      /\/\*\* Map pin colors[\s\S]*?export const SEARCH_FOCUS_PIN_COLOR = "[^"]*";/,
      pinColorsBlock.trimEnd(),
    );
    await writeFile(WEB_MAP_PIN_PATH, updated);
  }

  // overlayStack.ts Z scale — generated from tokens.zIndex (issue #60).
  const overlayStack = await readFile(WEB_OVERLAY_STACK_PATH, "utf8");
  const zEntries = Object.entries(tokens.zIndex)
    .map(([key, value]) => `  ${key}: ${Number(value)},`)
    .join("\n");
  const zBlock = `// Values generated from design/tokens.json zIndex — edit there, then npm run tokens:generate.
export const Z = {
${zEntries}
} as const;`;
  const zPattern =
    /(?:\/\/ Values generated from design\/tokens\.json zIndex[^\n]*\n)?export const Z = \{[\s\S]*?\} as const;/;
  if (!zPattern.test(overlayStack)) {
    console.warn("overlayStack.ts: Z block not found — skip patch");
  } else {
    await writeFile(WEB_OVERLAY_STACK_PATH, overlayStack.replace(zPattern, zBlock));
  }

  console.log("Generated:");
  console.log(`  ${path.relative(ROOT, WEB_CSS_PATH)}`);
  console.log(`  ${path.relative(ROOT, IOS_COLORS_DIR)}`);
  console.log(`  ${path.relative(ROOT, IOS_THEME_PATH)}`);
  console.log(`  ${path.relative(ROOT, IOS_TTF_TIER_PATH)}`);
  console.log(`  ${path.relative(ROOT, WEB_TTF_TIER_PATH)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
